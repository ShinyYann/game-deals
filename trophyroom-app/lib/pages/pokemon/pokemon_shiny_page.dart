                    decoration: BoxDecoration(
                      color: Colors.white.withAlpha(8),
                      borderRadius: const BorderRadius.vertical(bottom: Radius.circular(14)),
                    ),
                    child: Icon(Icons.delete_outline, color: Colors.red[900], size: 16),
                  ),
                ),
              ],
            ),
          ),
        );
      },
    );
  }
}

// ═══════════════════════════════════════════════════
// 添加页面（全屏搜索 + 选择 + 备注）
// ═══════════════════════════════════════════════════

class _ShinyAddScreen extends StatefulWidget {
  final Set<int> existing;
  final void Function(_ShinyEntry) onAdd;

  const _ShinyAddScreen({required this.existing, required this.onAdd});

  @override
  State<_ShinyAddScreen> createState() => _ShinyAddScreenState();
}

class _ShinyAddScreenState extends State<_ShinyAddScreen> {
  final _searchCtrl = TextEditingController();
  List<Map<String, dynamic>> _results = [];
  Map<String, dynamic>? _selected;
  final _noteCtrl = TextEditingController();
  bool _showGrid = true;

  @override
  void dispose() {
    _searchCtrl.dispose();
    _noteCtrl.dispose();
    super.dispose();
  }

  void _onSearch(String q) {
    if (q.isEmpty) {
      setState(() {
        _results = [];
        _showGrid = true;
        _selected = null;
      });
      return;
    }
    final query = q.toLowerCase();
    setState(() {
      _showGrid = false;
      _selected = null;
      _results = _allPokemon.where((p) {
        final z = (p['z'] as String? ?? '').toLowerCase();
        final n = p['n']?.toString() ?? '';
        return z.contains(query) || n.contains(query);
      }).take(30).toList();
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: const Color(0xFF0F0F23),
      appBar: AppBar(
        backgroundColor: const Color(0xFF0F0F23),
        elevation: 0,
        leading: IconButton(
          icon: const Icon(Icons.arrow_back, color: Colors.white),
          onPressed: () => Navigator.pop(context),
        ),
        title: const Text('添加闪光', style: TextStyle(color: Colors.white, fontWeight: FontWeight.bold)),
      ),
      body: Column(
        children: [
          Padding(
            padding: const EdgeInsets.fromLTRB(12, 4, 12, 8),
            child: TextField(
              controller: _searchCtrl,
              autofocus: true,
              style: const TextStyle(color: Colors.white, fontSize: 15),
              decoration: InputDecoration(
                hintText: '输入中文名或编号搜索...',
                hintStyle: TextStyle(color: Colors.grey[500], fontSize: 13),
                prefixIcon: Icon(Icons.search, color: Colors.grey[400], size: 20),
                suffixIcon: _searchCtrl.text.isNotEmpty
                    ? IconButton(
                        icon: const Icon(Icons.clear, color: Colors.grey, size: 18),
                        onPressed: () {
                          _searchCtrl.clear();
                          _onSearch('');
                        },
                      )
                    : null,
                filled: true,
                fillColor: const Color(0xFF1E1E35),
                border: OutlineInputBorder(
                  borderRadius: BorderRadius.circular(12),
                  borderSide: BorderSide.none,
                ),
                contentPadding: const EdgeInsets.symmetric(vertical: 12),
              ),
              onChanged: _onSearch,
            ),
          ),

          if (_selected != null) _buildSelectedPreview(),

          Expanded(child: _showGrid ? _buildGenGrid() : _buildSearchResults()),
        ],
      ),
    );
  }

  Widget _buildGenGrid() {
    final gens = <int, List<Map<String, dynamic>>>{};
    for (final p in _allPokemon) {
      final n = p['n'] as int;
      int gen;
      if (n <= 151) gen = 1;
      else if (n <= 251) gen = 2;
      else if (n <= 386) gen = 3;
      else if (n <= 493) gen = 4;
      else if (n <= 649) gen = 5;
      else if (n <= 721) gen = 6;
      else if (n <= 809) gen = 7;
      else if (n <= 898) gen = 8;
      else gen = 9;
      gens.putIfAbsent(gen, () => []);
      gens[gen]!.add(p);
    }

    const genNames = {1: '关都', 2: '城都', 3: '丰缘', 4: '神奥', 5: '合众', 6: '卡洛斯', 7: '阿罗拉', 8: '伽勒尔', 9: '帕底亚'};

    return CustomScrollView(
      slivers: [
        SliverPadding(
          padding: const EdgeInsets.only(left: 4, bottom: 8),
          sliver: SliverToBoxAdapter(
            child: Text(
              '按世代浏览，或搜索名字/编号',
              style: TextStyle(color: Colors.grey[500], fontSize: 13),
            ),
          ),
        ),
        for (int g = 1; g <= 9; g++)
          SliverToBoxAdapter(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Padding(
                  padding: const EdgeInsets.symmetric(vertical: 6, horizontal: 4),
                  child: Row(children: [
                    Container(
                      padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 2),
                      decoration: BoxDecoration(
                        color: Colors.amberAccent.withAlpha(30),
                        borderRadius: BorderRadius.circular(8),
                      ),
                      child: Text('第${genNames[g] ?? g}世代',
                          style: const TextStyle(color: Colors.amberAccent, fontSize: 12, fontWeight: FontWeight.w600)),
                    ),
                    const SizedBox(width: 8),
                    Text('${(gens[g] ?? []).length} 只', style: TextStyle(color: Colors.grey[600], fontSize: 11)),
                  ]),
                ),
                Wrap(
                  spacing: 4,
                  runSpacing: 4,
                  children: (gens[g] ?? []).map((p) => _miniChip(p)).toList(),
                ),
                const SizedBox(height: 8),
              ],
            ),
          ),
      ],
    );
  }

  Widget _buildSearchResults() {
    if (_results.isEmpty) {
      return Center(
        child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            Icon(Icons.search_off, color: Colors.grey[600], size: 40),
            const SizedBox(height: 8),
            Text('没有找到', style: TextStyle(color: Colors.grey[500], fontSize: 14)),
          ],
        ),
      );
    }
    return ListView.builder(
      padding: const EdgeInsets.symmetric(horizontal: 8),
      itemCount: _results.length,
      itemBuilder: (ctx, i) {
        final p = _results[i];
        final already = widget.existing.contains(p['n']);
        final isSelected = _selected == p;
        return Container(
          margin: const EdgeInsets.symmetric(vertical: 2),
          decoration: BoxDecoration(
            color: isSelected ? Colors.amber.withAlpha(25) : already ? Colors.green.withAlpha(15) : null,
            borderRadius: BorderRadius.circular(10),
            border: isSelected ? Border.all(color: Colors.amberAccent.withAlpha(80), width: 1.5) : null,
          ),
          child: ListTile(
            dense: true,
            leading: ClipRRect(
              borderRadius: BorderRadius.circular(6),
              child: Image.network(
                'http://8.153.97.56/api/poke/sprite/${p['n']}',
                height: 36, width: 36,
                errorBuilder: (_, __, ___) => const Icon(Icons.catching_pokemon, color: Colors.white38, size: 28),
              ),
            ),
            title: Text(
              '${p['z']}',
              style: TextStyle(
                color: already ? Colors.green[400] : Colors.white,
                fontWeight: FontWeight.w600, fontSize: 15,
              ),
            ),
            subtitle: Text(
              '#${p['n']}${already ? '  ·  已有' : ''}',
              style: TextStyle(color: already ? Colors.green[300] : Colors.grey[500], fontSize: 11),
            ),
            trailing: isSelected
                ? const Icon(Icons.check_circle, color: Colors.amberAccent, size: 22)
                : already
                    ? const Icon(Icons.check, color: Colors.green, size: 20)
                    : const Icon(Icons.add_circle_outline, color: Colors.white38, size: 20),
            onTap: already ? null : () => setState(() => _selected = p),
          ),
        );
      },
    );
  }

  Widget _miniChip(Map<String, dynamic> p) {
    final already = widget.existing.contains(p['n']);
    return GestureDetector(
      onTap: already ? null : () => setState(() => _selected = p),
      child: Container(
        width: 44, height: 44,
        decoration: BoxDecoration(
          color: already ? Colors.green.withAlpha(15) : Colors.white.withAlpha(6),
          borderRadius: BorderRadius.circular(8),
          border: Border.all(color: already ? Colors.green.withAlpha(40) : Colors.white.withAlpha(15)),
        ),
        child: Stack(
          children: [
            Center(
              child: ClipRRect(
                borderRadius: BorderRadius.circular(4),
                child: Image.network(
                  'http://8.153.97.56/api/poke/sprite/${p['n']}',
                  height: 32, width: 32,
                  errorBuilder: (_, __, ___) => const SizedBox(),
                ),
              ),
            ),
            if (already)
              const Positioned(top: 2, right: 2,
                child: Icon(Icons.check_circle, color: Colors.green, size: 12)),
          ],
        ),
      ),
    );
  }

  Widget _buildSelectedPreview() {
    final p = _selected!;
    final ndex = p['n'] as int;
    final name = p['z'] as String;

    return Container(
      margin: const EdgeInsets.symmetric(horizontal: 12),
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        gradient: LinearGradient(
          colors: [Colors.amber.withAlpha(20), Colors.amber.withAlpha(5)],
          begin: Alignment.topLeft, end: Alignment.bottomRight,
        ),
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: Colors.amberAccent.withAlpha(60)),
      ),
      child: Column(
        children: [
          Row(
            children: [
              ClipRRect(
                borderRadius: BorderRadius.circular(8),
                child: Image.network(
                  'http://8.153.97.56/api/poke/sprite/shiny/$ndex',
                  height: 48, width: 48,
                  errorBuilder: (_, __, ___) => const Icon(Icons.auto_awesome, color: Colors.amberAccent, size: 36),
                ),
              ),
              const SizedBox(width: 12),
              Expanded(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Row(children: [
                      const Text('✨ ', style: TextStyle(fontSize: 18)),
                      Text(name, style: const TextStyle(color: Colors.white, fontSize: 18, fontWeight: FontWeight.bold)),
                    ]),
                    Text('#$ndex', style: TextStyle(color: Colors.grey[400], fontSize: 12)),
                  ],
                ),
              ),
              ClipRRect(
                borderRadius: BorderRadius.circular(8),
                child: Image.network(
                  'http://8.153.97.56/api/poke/sprite/$ndex',
                  height: 36, width: 36,
                  errorBuilder: (_, __, ___) => const SizedBox(),
                ),
              ),
              const SizedBox(width: 4),
              Text('普通', style: TextStyle(color: Colors.grey[500], fontSize: 9)),
            ],
          ),
          const SizedBox(height: 12),
          TextField(
            controller: _noteCtrl,
            maxLines: 2,
            style: const TextStyle(color: Colors.white, fontSize: 13),
            decoration: InputDecoration(
              hintText: '备注（选填）比如：蛋孵化第318只出、跟随我3年了...',
              hintStyle: TextStyle(color: Colors.grey[600], fontSize: 12),
              filled: true,
              fillColor: const Color(0xFF0D0D1A),
              border: OutlineInputBorder(
                borderRadius: BorderRadius.circular(8),
                borderSide: BorderSide.none,
              ),
              contentPadding: const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
            ),
          ),
          const SizedBox(height: 8),
          SizedBox(
            width: double.infinity,
            child: ElevatedButton(
              onPressed: () {
                widget.onAdd(_ShinyEntry(
                  ndex: ndex,
                  name: name,
                  note: _noteCtrl.text.trim(),
                ));
              },
              style: ElevatedButton.styleFrom(
                backgroundColor: Colors.amber.shade700,
                foregroundColor: Colors.white,
                shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(10)),
                padding: const EdgeInsets.symmetric(vertical: 12),
              ),
              child: const Text('✨ 添加闪光', style: TextStyle(fontWeight: FontWeight.bold, fontSize: 15)),
            ),
          ),
        ],
      ),
    );
  }
}
