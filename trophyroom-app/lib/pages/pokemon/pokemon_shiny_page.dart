class _ShinyEntry {
  final int ndex;
  final String name;
  String note;
  final DateTime addedAt;

  _ShinyEntry({
    required this.ndex,
    required this.name,
    this.note = '',
    DateTime? addedAt,
  }) : addedAt = addedAt ?? DateTime.now();

  factory _ShinyEntry.fromJson(Map<String, dynamic> json) => _ShinyEntry(
        ndex: json['n'] as int,
        name: json['z'] as String,
        note: json['note'] as String? ?? '',
        addedAt: json['ts'] != null
            ? DateTime.tryParse(json['ts'] as String) ?? DateTime.now()
            : DateTime.now(),
      );

  Map<String, dynamic> toJson() => {
        'n': ndex,
        'z': name,
        'note': note,
        'ts': addedAt.toIso8601String(),
      };
}

class _PokemonShinyPageState extends State<PokemonShinyPage>
    with SingleTickerProviderStateMixin {
  List<_ShinyEntry> _collection = [];
  late AnimationController _glowCtrl;

  @override
  void initState() {
    super.initState();
    _glowCtrl = AnimationController(
      vsync: this,
      duration: const Duration(seconds: 2),
    )..repeat(reverse: true);
    _loadCollection();
  }

  @override
  void dispose() {
    _glowCtrl.dispose();
    super.dispose();
  }

  Future<void> _loadCollection() async {
    final prefs = await SharedPreferences.getInstance();
    final raw = prefs.getString('shiny_v2') ?? '[]';
    setState(() {
      _collection = (jsonDecode(raw) as List)
          .map((e) => _ShinyEntry.fromJson(e as Map<String, dynamic>))
          .toList();
    });
  }

  Future<void> _saveCollection() async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(
      'shiny_v2',
      jsonEncode(_collection.map((e) => e.toJson()).toList()),
    );
  }

  void _deleteEntry(int i) {
    setState(() => _collection.removeAt(i));
    _saveCollection();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            Text('✨', style: TextStyle(fontSize: 20)),
            SizedBox(width: 8),
            Text('闪光收藏', style: TextStyle(fontWeight: FontWeight.bold)),
          ],
        ),
        backgroundColor: const Color(0xFF0F0F23),
        actions: [
          AnimatedBuilder(
            animation: _glowCtrl,
            builder: (ctx, _) {
              final glow = 0.6 + 0.4 * sin(_glowCtrl.value * pi);
              return GestureDetector(
                onTap: () => _showAddScreen(context),
                child: Container(
                  margin: const EdgeInsets.only(right: 4),
                  padding: const EdgeInsets.symmetric(horizontal: 14, vertical: 6),
                  decoration: BoxDecoration(
                    gradient: LinearGradient(
                      colors: [
                        Colors.amber.withAlpha((80 * glow).round()),
                        Colors.amber.withAlpha((120 * glow).round()),
                      ],
                    ),
                    borderRadius: BorderRadius.circular(20),
                    border: Border.all(
                      color: Colors.amberAccent.withAlpha((100 * glow).round()),
                      width: 1.5,
                    ),
                  ),
                  child: Row(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      Icon(Icons.add, color: Colors.amber[200], size: 18),
                      const SizedBox(width: 4),
                      Text('添加', style: TextStyle(color: Colors.amber[200], fontSize: 13, fontWeight: FontWeight.w600)),
                    ],
                  ),
                ),
              );
            },
          ),
          const SizedBox(width: 8),
        ],
      ),
      backgroundColor: const Color(0xFF0F0F23),
      body: _collection.isEmpty ? _buildEmptyState() : _buildGrid(),
    );
  }

  Widget _buildEmptyState() {
    return Center(
      child: Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          const Text('✨', style: TextStyle(fontSize: 72)),
          const SizedBox(height: 16),
          Text(
            '还没有收集到闪光',
            style: TextStyle(color: Colors.grey[400], fontSize: 16),
          ),
          const SizedBox(height: 8),
          Text(
            '点击右上角 ✨ 添加第一只闪光伙伴',
            style: TextStyle(color: Colors.grey[600], fontSize: 13),
          ),
          const SizedBox(height: 24),
          ElevatedButton.icon(
            onPressed: () => _showAddScreen(context),
            icon: const Icon(Icons.add, size: 18),
            label: const Text('添加闪光'),
            style: ElevatedButton.styleFrom(
              backgroundColor: Colors.amber.shade700,
              foregroundColor: Colors.white,
              shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildGrid() {
    return Padding(
      padding: const EdgeInsets.fromLTRB(8, 8, 8, 80),
      child: GridView.builder(
        gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
          crossAxisCount: 2,
          childAspectRatio: 0.78,
          crossAxisSpacing: 8,
          mainAxisSpacing: 8,
        ),
        itemCount: _collection.length,
        itemBuilder: (ctx, i) => _ShinyCard(
          entry: _collection[i],
          glowCtrl: _glowCtrl,
          onDelete: () => _confirmDelete(ctx, i),
          onTap: () => _editNote(ctx, i),
        ),
      ),
    );
  }

  void _confirmDelete(BuildContext ctx, int i) {
    final e = _collection[i];
    showDialog(
      context: ctx,
      builder: (c) => AlertDialog(
        backgroundColor: const Color(0xFF1A1A2E),
        title: Text('删除 ${e.name}?', style: const TextStyle(color: Colors.white)),
        content: const Text('这个操作不可撤销', style: TextStyle(color: Colors.white70)),
        actions: [
          TextButton(onPressed: () => Navigator.pop(c), child: const Text('取消', style: TextStyle(color: Colors.grey))),
          TextButton(
            onPressed: () { Navigator.pop(c); _deleteEntry(i); },
            child: const Text('删除', style: TextStyle(color: Colors.redAccent)),
          ),
        ],
      ),
    );
  }

  void _editNote(BuildContext ctx, int i) {
    final e = _collection[i];
    final ctrl = TextEditingController(text: e.note);
    showDialog(
      context: ctx,
      builder: (c) => AlertDialog(
        backgroundColor: const Color(0xFF1A1A2E),
        title: Text('${e.name} 备注', style: const TextStyle(color: Colors.white)),
        content: TextField(
          controller: ctrl,
          maxLines: 3,
          style: const TextStyle(color: Colors.white),
          decoration: InputDecoration(
            hintText: '输入备注...',
            hintStyle: TextStyle(color: Colors.grey[500]),
            filled: true,
            fillColor: const Color(0xFF0D0D1A),
            border: OutlineInputBorder(borderRadius: BorderRadius.circular(8), borderSide: BorderSide.none),
          ),
        ),
        actions: [
          TextButton(onPressed: () => Navigator.pop(c), child: const Text('取消', style: TextStyle(color: Colors.grey))),
          ElevatedButton(
            onPressed: () {
              setState(() => _collection[i].note = ctrl.text.trim());
              _saveCollection();
              Navigator.pop(c);
            },
            style: ElevatedButton.styleFrom(backgroundColor: Colors.amber.shade700),
            child: const Text('保存', style: TextStyle(color: Colors.white)),
          ),
        ],
      ),
    );
  }

  void _showAddScreen(BuildContext ctx) {
    Navigator.push(
      ctx,
      MaterialPageRoute(
        builder: (_) => _ShinyAddScreen(
          existing: _collection.map((e) => e.ndex).toSet(),
          onAdd: (entry) {
            setState(() => _collection.add(entry));
            _saveCollection();
            Navigator.of(ctx).pop();
          },
        ),
      ),
    );
  }
}

